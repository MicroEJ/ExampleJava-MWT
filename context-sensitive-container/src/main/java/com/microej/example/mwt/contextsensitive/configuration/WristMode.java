/**
 * Defines the way the user wears the watch: on the left-arm or on the right-arm.
 */
public enum WristMode {

	/**
	 * Use the watch on the left arm.
	 * <p>
	 * This is the default setting.
	 */
	LEFT,

	/**
	 * Use the watch on the right arm.
	 */
	RIGHT;

}
